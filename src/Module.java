public class Module {
    // Instance variable to store the mark of the module
    private int mark;

    // Method to return the mark of the module
    public int getMark() {
        return mark;
    }

    // Method to set the mark of the module
    public void setMark(int mark) {
        // Assign the provided mark to the module's mark variable
        this.mark = mark;
    }
}